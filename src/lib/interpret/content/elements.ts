/**
 * 여덟 글자의 오행 분포를 실제 개수와 형태로 서술한다.
 * 지배 오행(월지 득령 가중)의 짙기 + 성향 + 옅은 오행을 '여백'으로 다정하게 안내.
 * 동률이면 "나란히 흐른다"로 — 임의로 한쪽을 단정하지 않는다(명리적 정직함).
 */
export function ELEMENT_BALANCE_TEXT(el: ProfileContext["elements"], voice: Voice = "yo"): string {
  const strong = el.dominant;
  const co = el.coDominant ?? [strong]; // 구버전 캐시(coDominant 없음) 호환
  const sk = SKELETON[voice];
  const trait = ELEMENT_TRAIT[voice][strong];
  const season = ELEMENT_SEASON[voice][strong];
  let s: string;
  if (co.length > 1) {
    s = `${sk.tie(co.join("·"), strong)}${trait} ${sk.now}${season}`;
  } else {
    s = `${sk.single(strong, el.counts[strong])}${trait} ${sk.now}${season}`;
  }
  s += el.lacking.length ? sk.lacking(el.lacking.join("·")) : sk.balanced;
  return s;
}
