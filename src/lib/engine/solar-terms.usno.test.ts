// 허용 오차: ΔT(TT−UT)는 과거는 관측으로 확정되지만 미래는 '예측'이라, 같은 천문 계산이라도
// UT로 환산한 시각이 예측 모델에 따라 1분 안팎 갈린다(2030년대는 불확실성이 더 크다).
// 이 테스트의 목적은 그 수십 초를 다투는 게 아니라 gross 오차를 잡는 것이다 —
// 교체 전 Meeus 저차 근사 테이블은 이 표와 최대 12분까지 어긋났고, 그건 월주를 뒤집는 크기다.
const DELTA_T_OBSERVED_THROUGH = 2026;
const toleranceMin = (year: number) => (year <= DELTA_T_OBSERVED_THROUGH ? 1 : 2);

/** 춘분·하지·추분·동지의 절기 인덱스 (황경 = (285 + 15*idx) % 360) */
const CARDINAL: Array<{ idx: number; name: string; col: 1 | 2 | 3 | 4 }> = [
  { idx: 5, name: "춘분", col: 1 },
  { idx: 11, name: "하지", col: 2 },
  { idx: 17, name: "추분", col: 3 },
  { idx: 23, name: "동지", col: 4 },
];

const utcMs = (year: number, mdhm: string) => {
  const [md, hm] = mdhm.split(" ");
  return Date.parse(`${year}-${md}T${hm}:00Z`);
};

describe("절기 테이블 × USNO 분점·지점 (외부 대조)", () => {
  it("2016~2036년 춘분·하지·추분·동지가 USNO 공표 시각과 일치한다 (확정 ΔT 구간 1분, 예측 구간 2분)", () => {
    const off: string[] = [];
    for (const row of USNO) {
      const year = row[0];
      for (const c of CARDINAL) {
        const expectMs = utcMs(year, row[c.col]);
        const actualMs = termInstant(year, c.idx).getTime();
        const diffMin = Math.abs(actualMs - expectMs) / 60_000;
        if (diffMin > toleranceMin(year)) {
          off.push(
            `${year} ${c.name}: 테이블 ${new Date(actualMs).toISOString()} vs USNO ${new Date(expectMs).toISOString()} (${diffMin.toFixed(1)}분)`,
          );
        }
      }
    }
    expect(off, `USNO 대비 허용 오차 초과:\n${off.join("\n")}`).toEqual([]);
  });
});
