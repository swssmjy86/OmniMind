// 한국 서머타임(일광절약시간) 시행 구간 — 벽시계가 표준시+1h 이던 기간.
// 이 구간 출생은 시계가 앞당겨져 있었으므로 −1h 하여 실제 표준시로 되돌린 뒤 사주를 세운다.
// 구간 경계(월·일·시각)는 국가기록원/KASI 기록 기준. 시행 요약:
//   1948~1951, 1955~1960 (이승만 정부), 1987~1988 (서울올림픽 대비).
// 각 연도 시행 일자가 상이하므로 정밀 일시로 고정한다. (타깃 1990+ 세대엔 무영향)
export interface DstPeriod {
  start: string; // KST 벽시계 시작(이 시각부터 +1h 적용)
  end: string; // KST 벽시계 종료(이 시각에 원위치)
}

// KST 벽시계 문자열로 저장. 1948~1960은 IANA tzdata(Rule ROK)·국가기록원 기록으로 확정:
//   1948~51 시행일 상이(6/1·4/3·4/1·5/6), 해제는 9월 토요일 24:00(1949~51) 규칙.
//   1955~56 개별 지정, 1957~60은 5월 첫 일요일 0시 시행·9월 18일 이후 일요일 0시 해제.
// 모든 경계가 벽시계 기준이라 +09:00 파싱과 일관되게 비교된다.
// (참고: 1954-03-21~1961-08-09 한국 표준시는 UTC+8:30 — 이 30분 이슈는 서머타임과 별개의
//  잔여 과제로, 시주 경계가 바뀔 수 있어 외부 만세력 대조 후 별도 반영한다.)
export const DST_PERIODS: DstPeriod[] = [
  { start: "1948-06-01T00:00", end: "1948-09-13T00:00" },
  { start: "1949-04-03T00:00", end: "1949-09-11T00:00" },
  { start: "1950-04-01T00:00", end: "1950-09-10T00:00" },
  { start: "1951-05-06T00:00", end: "1951-09-09T00:00" },
  { start: "1955-05-05T00:00", end: "1955-09-09T00:00" },
  { start: "1956-05-20T00:00", end: "1956-09-30T00:00" },
  { start: "1957-05-05T00:00", end: "1957-09-22T00:00" },
  { start: "1958-05-04T00:00", end: "1958-09-21T00:00" },
  { start: "1959-05-03T00:00", end: "1959-09-20T00:00" },
  { start: "1960-05-01T00:00", end: "1960-09-18T00:00" },
  { start: "1987-05-10T02:00", end: "1987-10-11T03:00" },
  { start: "1988-05-08T02:00", end: "1988-10-09T03:00" },
];

const toEpoch = (kst: string) => Date.parse(`${kst}:00+09:00`);
const PERIODS = DST_PERIODS.map((p) => ({ start: toEpoch(p.start), end: toEpoch(p.end) }));

/** 서머타임 구간 출생이면 −1h 보정한 실제 표준시 instant 반환. */
export function applyDst(instant: Date): Date {
  const t = instant.getTime();
  for (const p of PERIODS) {
    if (t >= p.start && t < p.end) return new Date(t - 3600_000);
  }
  return instant;
}
